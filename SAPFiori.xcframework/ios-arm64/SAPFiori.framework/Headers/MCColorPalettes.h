//
// Series palette. Colors that affect the series.
//
struct MCSeriesPalette
{
    unsigned int color;                  /// lineColor
    unsigned int colorAdjunct;           /// labelColor
    unsigned int positiveColor;          /// positiveMinColor
    unsigned int positiveColorAdjunct;   /// positiveMaxColor
    unsigned int negativeColor;          /// negativeMinColor
    unsigned int negativeColorAdjunct;   /// negativeMaxColor
    unsigned int neutralColor;
    unsigned int neutralColorAdjunct;
};
typedef struct MCSeriesPalette MCSeriesPalette;

//
// Chart palette. Colors that affect the chart.
//
struct MCPalette
{
    unsigned int trendColorGood;
    unsigned int trendColorBad;
    unsigned int trendColorNeutral;
    unsigned int seriesPaletteCount;
    MCSeriesPalette* seriesPalettes;
};
typedef struct MCPalette MCPalette;

